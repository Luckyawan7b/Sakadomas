        document.addEventListener('DOMContentLoaded', function() {
            const filterButtons = document.querySelectorAll('.no-scrollbar + div select, .overflow-x-auto button');
            const categoryBtns = document.querySelectorAll('.overflow-x-auto button');
            const cards = document.querySelectorAll('[data-jenis]');
            const searchInput = document.querySelector('input[placeholder*="Cari"]');
            const countDisplay = document.querySelector('.text-primary.font-bold');
            const sidebarCheckboxes = document.querySelectorAll('aside input[type="checkbox"]');
            let activeFilter = 'semua';

            // Category pill filter
            categoryBtns.forEach(btn => {
                btn.addEventListener('click', function() {
                    categoryBtns.forEach(b => {
                        b.classList.remove('bg-primary', 'text-on-primary');
                        b.classList.add('bg-surface-variant', 'text-on-surface-variant');
                    });
                    this.classList.remove('bg-surface-variant', 'text-on-surface-variant');
                    this.classList.add('bg-primary', 'text-on-primary');
                    activeFilter = this.textContent.trim().toLowerCase();
                    applyFilters();
                });
            });

            // Sidebar checkbox filter
            sidebarCheckboxes.forEach(cb => {
                cb.addEventListener('change', applyFilters);
            });

            // Search filter
            if (searchInput) {
                searchInput.addEventListener('input', applyFilters);
            }

            function applyFilters() {
                const searchTerm = searchInput ? searchInput.value.toLowerCase() : '';
                let visibleCount = 0;

                cards.forEach(card => {
                    const jenis = card.dataset.jenis || '';
                    const kelamin = card.dataset.kelamin || '';
                    const title = card.querySelector('h4') ? card.querySelector('h4').textContent
                        .toLowerCase() : '';
                    const id = card.querySelector('.text-xs.font-bold') ? card.querySelector(
                        '.text-xs.font-bold').textContent.toLowerCase() : '';

                    let showByCategory = activeFilter === 'semua';
                    if (activeFilter === 'cross texel') showByCategory = jenis === 'cross-texel';
                    if (activeFilter === 'merino') showByCategory = jenis === 'merino';
                    if (activeFilter === 'etawa') showByCategory = jenis === 'etawa';
                    if (activeFilter === 'jantan') showByCategory = kelamin === 'jantan';
                    if (activeFilter === 'betina') showByCategory = kelamin === 'betina';

                    let showBySearch = !searchTerm || title.includes(searchTerm) || id.includes(
                        searchTerm) || jenis.includes(searchTerm);

                    if (showByCategory && showBySearch) {
                        card.style.display = '';
                        card.style.opacity = '0';
                        card.style.transform = 'translateY(20px)';
                        setTimeout(() => {
                            card.style.transition = 'opacity 0.4s ease, transform 0.4s ease';
                            card.style.opacity = '1';
                            card.style.transform = 'translateY(0)';
                        }, 50);
                        visibleCount++;
                    } else {
                        card.style.display = 'none';
                    }
                });
                if (countDisplay) countDisplay.textContent = visibleCount;
            }

            // Reset button
            const resetBtn = document.querySelector('button.underline');
            if (resetBtn) {
                resetBtn.addEventListener('click', function() {
                    sidebarCheckboxes.forEach(cb => cb.checked = false);
                    if (searchInput) searchInput.value = '';
                    categoryBtns.forEach(b => {
                        b.classList.remove('bg-primary', 'text-on-primary');
                        b.classList.add('bg-surface-variant', 'text-on-surface-variant');
                    });
                    categoryBtns[0].classList.remove('bg-surface-variant', 'text-on-surface-variant');
                    categoryBtns[0].classList.add('bg-primary', 'text-on-primary');
                    activeFilter = 'semua';
                    applyFilters();
                });
            }
        });
    </script>
